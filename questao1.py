# programa para calcular o imc dos alutos de uma academia 
# O calculo do IMC é imc=peso/altura² 

print('Bem vamos começar a calcular o seu IMC')
print(' 1°Digite o seu nome')
nome=str(input( ))

print('Digite o seu peso')
peso=float(input( ))
print("Agora digite a sua Altura")
altura=float(input( ))

imc= peso/(altura*altura)
print("O seu imc" ,nome, 'é de',imc)





       


